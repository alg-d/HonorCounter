using System.Windows.Shapes;

namespace HonorCounter
{
    /// <summary>
    /// CheckWindow.xaml の相互作用ロジック
    /// </summary>
    public partial class CheckWindow : Window
    {
        [DllImport("gdi32.dll", EntryPoint = "DeleteObject")]
        [return: MarshalAs(UnmanagedType.Bool)]
        public static extern bool DeleteObject([In] IntPtr hObject);

        public CheckWindow(System.Drawing.Bitmap bitmap)
        {
            InitializeComponent();

            var handle = bitmap.GetHbitmap();
            try
            {
                image.Source = Imaging.CreateBitmapSourceFromHBitmap(handle, IntPtr.Zero, Int32Rect.Empty, BitmapSizeOptions.FromEmptyOptions());
            }
            finally
            {
                DeleteObject(handle);
            }
        }
    }
}
